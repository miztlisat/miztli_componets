///
/// Aquí puedes agregar más detalles sobre lo que hace la clase.
class MiClase {
  int numero;

  /// Constructor de la clase MiClase.
  MiClase(this.numero);

  /// Método que duplica el número actual.
  int duplicar() {
    return numero * 2;
  }
}

